    private void btnXuatEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXuatEActionPerformed
        try{
            JFileChooser jFileChooser = new JFileChooser();
            FileNameExtensionFilter filter = new FileNameExtensionFilter("Excel", "xlsx");
            jFileChooser.setFileFilter(filter);
            jFileChooser.showSaveDialog(this);
            File saveFile = jFileChooser.getSelectedFile();

            if(saveFile != null){
                saveFile = new File(saveFile.toString() +".xlsx");
                Workbook wb = new XSSFWorkbook();
                Sheet sheet = wb.createSheet("dsNhaCungCap");

                Row rowCol = sheet.createRow(0);
                for (int i = 0; i < tableNCC.getColumnCount(); i++) {
                    Cell cell = rowCol.createCell(i);
                    cell.setCellValue(tableNCC.getColumnName(i));
                }

                for (int j = 0; j < tableNCC.getRowCount(); j++) {
                    Row row = sheet.createRow(j+1);
                    for (int k = 0; k < tableNCC.getColumnCount(); k++) {
                        Cell cell = row.createCell(k);
                        if (tableNCC.getValueAt(j, k)!=null){
                            cell.setCellValue(tableNCC.getValueAt(j, k).toString());
                        }
                    }
                }
                FileOutputStream out = new FileOutputStream(new File(saveFile.toString()));
                wb.write(out);
                wb.close();
                out.close();
                openFile(saveFile.toString());
            }else{
                JOptionPane.showMessageDialog(null, "Error al generar archivo");
            }
        }catch(FileNotFoundException e){
            System.out.println(e);
        }catch(IOException io){
            System.out.println(io);
        }
    }//GEN-LAST:event_btnXuatEActionPerformed

