    private void btnXacNhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXacNhanActionPerformed
        if(tfMaNCC.getText().trim().equals("") || tfTenNCC.getText().trim().equals("") ||tfDC.getText().trim().equals("") ||tfSDT.getText().trim().equals("")){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đủ thông tin");
        }
        else if(!(ValidateMaNCC(tfMaNCC.getText().trim()))){
            JOptionPane.showMessageDialog(this, "Mã nhà cung cấp không hợp lệ");
        }
        else if(!(ValidateSDT(tfSDT.getText().trim()))){
            JOptionPane.showMessageDialog(this, "Số điện thoại không hợp lệ");
        }
        else if(temp == 1) {
            NhaCungCapDTO ncc = new NhaCungCapDTO();
            ncc.setMaNCC(tfMaNCC.getText());
            ncc.setTenNCC(tfTenNCC.getText());
            ncc.setDiaChiNCC(tfDC.getText());
            ncc.setSDT(tfSDT.getText());

            JOptionPane.showMessageDialog(this, nccBUS.addNCC(ncc));
            model.setRowCount(0);
            loadData();
            clear();
        }
        else if (temp == 0){
            NhaCungCapDTO ncc = new NhaCungCapDTO();
            ncc.setMaNCC(tfMaNCC.getText());
            ncc.setTenNCC(tfTenNCC.getText());
            ncc.setDiaChiNCC(tfDC.getText());
            ncc.setSDT(tfSDT.getText());
            JOptionPane.showMessageDialog(this, nccBUS.updateNCC(ncc));

            model.setRowCount(0);
            loadData();
            clear();

        }
    }//GEN-LAST:event_btnXacNhanActionPerformed

