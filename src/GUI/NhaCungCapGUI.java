    private void loadData() {
        ArrayList<NhaCungCapDTO> arr = new ArrayList<NhaCungCapDTO>();
        nccBUS.loadNCC();

        arr = nccBUS.getdsncc();

        for (int i = 0; i < arr.size(); i++) {
            NhaCungCapDTO ncc = arr.get(i);
            String maNCC = ncc.getMaNCC();
            String tenNCC = ncc.getTenNCC();
            String diaChiNCC = ncc.getDiaChiNCC();
            String sdt = ncc.getSDT();
            model.addRow(new Object[]{maNCC,tenNCC,diaChiNCC,sdt});

        }

//        System.out.println(model.getColumnCount());
//        System.out.println(model.getRowCount());
    }
